#include <unistd.h>

void	ft_print_comb2(void)
{
	char	arr[5];

	arr[2] = ' ';
	arr[0] = '0';
	while (arr[0] <= '9')
	{
		arr[1] = '0';
		while (arr[1] <= '9')
		{
			arr[3] = arr[0] + (arr[1] + 1 - '0') / 10;
			while (arr[3] <= '9')
			{
				arr[4] = '0' + (arr[3] == arr[0]) * (arr[1] + 1 - '0');
				while (arr[4] <= '9')
				{
					write(1, arr, 5);
					write(1, ", ", 2 * !(arr[0] == '9' && arr[1] == '8'));
					arr[4] = arr[4] + 1;
				}
				arr[3] = arr[3] + 1;
			}
			arr[1] = arr[1] + 1;
		}
		arr[0] = arr[0] + 1;
	}
}
